package com.compfest.sea.usecase.product;

import java.math.BigDecimal;

public class Product {
    private Integer id, quantity, merchantId;
    private String name, description, category;
    private BigDecimal price;

    public Product(Integer id, Integer quantity, Integer merchantId, String name, String description, String category, BigDecimal price) {
        this.id = id;
        this.quantity = quantity;
        this.merchantId = merchantId;
        this.name = name;
        this.description = description;
        this.category = category;
        this.price = price;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getQuantity() {
        return quantity;
    }

    public void setQuantity(Integer quantity) {
        this.quantity = quantity;
    }

    public Integer getMerchantId() {
        return merchantId;
    }

    public void setMerchantId(Integer merchantId) {
        this.merchantId = merchantId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public BigDecimal getPrice() {
        return price;
    }

    public void setPrice(BigDecimal price) {
        this.price = price;
    }
}
